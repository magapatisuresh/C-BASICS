//printing all arithmetic operations:
#include<stdio.h>
void main()
{
	int a,b,s,d,p,q,m;
	a=10;
	b=2;
	
	s= a+b;
	printf("Sum of %d and %d is %d\n",a,b,s);
	
	d= a-b;
	printf("Difference of %d and %d is %d\n",a,b,d);
	
	p= a*b;
	printf("Product of %d and %d is %d\n",a,b,p);
	
	q= a/b;
	printf("Division of %d and %d is %d\n",a,b,q);
	
	m= a%b;
	printf("Modular division of %d and %d is %d",a,b,m);
	
	
	
	
	 
}
